                                <h3 style="margin-top: 20px;">Stain removals:</h3>
                                <div class="text">
                                    <p>Mattresses are prone to getting stained. We may have kids at home, who eat or drink on the bed, or have babies being put on our mattresses and sometimes they may end up wetting the bed. We must be cautious while cleaning the mattress. Memory foams and mattresses shouldn’t come in contact with water as it may affect the quality of the mattress. Cleaning with a stain remover is the best. Stain removers are easily available in the markets. For more tough stains like blood, vomit, urine and other stains, enzyme removers can be used to clean it up. An alternative for enzyme remover can be dish wash soap mixed with water. Dip a cloth in the mixture and slightly rub the stained area to clean. Remember a clean mattress is the key to healthy sleep</p>
                                </div>
                                <h3 style="margin-top: 20px;">Using baking soda:</h3>
                                <div class="text">
                                    <p>Almost every household will have stock of baking soda since it is an excellent cleaning agent. Sprinkle baking soda all over the mattress and leave it for several hours. Baking soda easily breaks down acids and any excess moisture or odour. The longer the baking soda sits on the mattress, the better it gets cleaned.</p>
                                </div>
                                <h3 style="margin-top: 20px;">Use a Vacuum again:</h3>
                                <div class="text">
                                    <p>Once the mattress has been sprinkled with baking soda and left for several hours, run the vacuum again so that any residue left from the baking soda gets cleaned</p>
