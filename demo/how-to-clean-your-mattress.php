                                </div>
                                <h3 style="margin-top: 20px;">Flip the mattress:</h3>
                                <div class="text">
                                    <p>Once, one side is cleaned well, flip the mattress and repat the vacuum process for the other side. Additionally, flipping the mattress to the other unused side is recommended every three months.</p>
                                  
