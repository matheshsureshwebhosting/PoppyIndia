                                <div class="text">
                                    <p>The first step is to completely strip the bed off its protectors and pillow cases, bed spreads and comforters. These can be machine washed if it is machine friendly fabric or can be dry cleaned. Washing in hot water will help remove dust mites and other dirt.</p>
                                </div>
                                <h3 style="margin-top: 20px;">Vacuum the mattress:</h3>
                                <div class="text">
                                    <p>We obviously cannot wash the mattress. Vacuuming the bed is the ideal option. This way, entire dust gets removed. Paying close attention to seams and stitches ensure any dirt/dust getting removed from the crevices.</p>
